#include<stdio.h>

using namespace std;

int main()
{
    int a;

    scanf("%d",&a);

    return 0;
}

/*
    printf is used to print things on the screen. Syntax:
    printf("format string",argument_list);

    Scanf is used to take input from the user. Syntax:
    scanf("format string",argument_list);

    scanf is faster than cin as scanf() explicitly declares the input type.
    format string:
    %c : character
    %d : integer
    %f : float
    %lf : double
    %s : string

*/

